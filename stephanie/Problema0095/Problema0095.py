n = input("Digite um número inteiro positivo: ")

if int(n)<0:
    print("Entrada Inválida!")
elif int(n)==0:
    print("Zero")
elif int(n)==1:
    print("Um")
elif int(n)==2:
    print("Dois")
elif int(n)==3:
    print("Três")
elif int(n)==4:
    print("Quatro")
elif int(n)==5:
    print("Cinco")
elif int(n)==6:
    print("Seis")
elif int(n)==7:
    print("Sete")
elif int(n)==8:
    print("Oito")
elif int(n)==9:
    print("Nove")
elif int(n)>9:
    print("Entrada Inválida!")
